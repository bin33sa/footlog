package com.fl.model;

public class ArenaHoursDTO {
	private Long arenaHoursCode;
	private Long arenaCode;
	private String startTime;
	private String endTime;
	private String operatingDate;
	
	
	public Long getArenaHoursCode() {
		return arenaHoursCode;
	}
	public void setArenaHoursCode(Long arenaHoursCode) {
		this.arenaHoursCode = arenaHoursCode;
	}
	public Long getArenaCode() {
		return arenaCode;
	}
	public void setArenaCode(Long arenaCode) {
		this.arenaCode = arenaCode;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getOperatingDate() {
		return operatingDate;
	}
	public void setOperatingDate(String operatingDate) {
		this.operatingDate = operatingDate;
	}
	
	
	
}
